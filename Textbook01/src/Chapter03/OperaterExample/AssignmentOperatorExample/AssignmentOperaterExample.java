package java.Chapter03.OperaterExample.AssignmentOperatorExample;

public class AssignmentOperaterExample {
    public static void main(String[] args) {
        int result = 0;
        result += 10;
        System.out.println("result = " + result);
        result -= 5;
        System.out.println("result = " + result);
        result *= 3;
        System.out.println("result = " + result);
        result /= 5;
        System.out.println("result = " + result);
        result %= 3;
        System.out.println("result = " + result);
    }
}
