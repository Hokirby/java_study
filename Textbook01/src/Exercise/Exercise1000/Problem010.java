package Exercise.Exercise1000;

public class Problem010 {
    public static void printSquare(int x) {
        System.out.println(x * x);
    }
    public static void main(String[] args) {
        int value = 2;
        System.out.println(value);
        printSquare(value);
        printSquare(3);
        printSquare(value * 2);
    }
}
